// ============================================================================
// isValidInternalCall (constant-time auth)
// ============================================================================

Deno.test('isValidInternalCall: rejects null auth header', () => {
  assertEquals(isValidInternalCall(null, 'some-service-key-that-is-long'), false)
})

Deno.test('isValidInternalCall: rejects when service key is not configured', () => {
  assertEquals(isValidInternalCall('Bearer some-token', undefined), false)
})

Deno.test('isValidInternalCall: rejects short service key', () => {
  assertEquals(isValidInternalCall('Bearer short', 'short'), false)
})

Deno.test('isValidInternalCall: rejects mismatched tokens', () => {
  const serviceKey = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.correct-key'
  assertEquals(isValidInternalCall('Bearer wrong-token-here-different', serviceKey), false)
})

Deno.test('isValidInternalCall: accepts matching bearer token', () => {
  const serviceKey = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.valid-service-key'
  assertEquals(isValidInternalCall(`Bearer ${serviceKey}`, serviceKey), true)
})

Deno.test('isValidInternalCall: rejects token that is a substring of service key', () => {
  const serviceKey = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.valid-service-key'
  const partial = serviceKey.slice(0, 30)
  assertEquals(isValidInternalCall(`Bearer ${partial}`, serviceKey), false)
})

Deno.test('isValidInternalCall: rejects includes-style bypass with extra content', () => {
  const serviceKey = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.valid-key'
  const padded = serviceKey + 'extra-chars'
  assertEquals(isValidInternalCall(`Bearer ${padded}`, serviceKey), false)
})
