Deno.test('escapePDF: escapes backslashes', () => {
  assertEquals(escapePDF('back\\slash'), 'back\\\\slash')
})

Deno.test('escapePDF: escapes parentheses', () => {
  assertEquals(escapePDF('(hello)'), '\\(hello\\)')
})

Deno.test('escapePDF: handles mixed special characters', () => {
  assertEquals(escapePDF('a\\b(c)d'), 'a\\\\b\\(c\\)d')
})

Deno.test('escapePDF: passes through safe strings', () => {
  assertEquals(escapePDF('Hello World 123'), 'Hello World 123')
})

Deno.test('escapePDF: handles empty string', () => {
  assertEquals(escapePDF(''), '')
})

